package loop.numberpattern;

public class OneTwoThree {

    public static void main(String[] args) {
        int j,i;
        int row=5;

        for(i=0;i<=row;i++){
            for(j=0;j<1;j++){
                System.out.print("12345");
            }
            System.out.println();
        }

    }
}
